#While Loops, it means that run a code until a condition is true, stop the loop when condition is false. we have to make a variable to use in condition

#Example 1
i = 0
while(i <= 10):
    print(i)
    i = i + 1

print("While Loop 1, finished")

#Example 2
j = 5
while(j >= 0):
    print(j)
    j = j - 1

print("While Loop 2, finished")

